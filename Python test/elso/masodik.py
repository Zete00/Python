x = float(input("Add meg a téglalap egyik oldalát: "))
y = float(input("Add meg a téglalap másik oldalát: "))

print(f"A téglalap kerülete = 2*(a+b) = 2*({x}+{y}) = {2*(x+y)}")
print(f"A téglalap területe = a*b = {x}*{y} = {x*y}")

